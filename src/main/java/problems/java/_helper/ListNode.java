package problems.java._helper;

public class ListNode {
    public int val;
    public ListNode next;

    // Constructors
    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}

